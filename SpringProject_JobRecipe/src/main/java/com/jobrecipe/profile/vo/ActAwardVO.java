package com.jobrecipe.profile.vo;

import java.util.Date;

public class ActAwardVO {

	private Date act_sdate;
	private Date act_edate;
	private int act_awardcheck;
	private String act_actawardname;
	private String act_description;
	
	public ActAwardVO(Date act_sdate, Date act_edate, int act_awardcheck, String act_actawardname,
			String act_description) {
		super();
		this.act_sdate = act_sdate;
		this.act_edate = act_edate;
		this.act_awardcheck = act_awardcheck;
		this.act_actawardname = act_actawardname;
		this.act_description = act_description;
	}

	public Date getAct_sdate() {
		return act_sdate;
	}

	public void setAct_sdate(Date act_sdate) {
		this.act_sdate = act_sdate;
	}

	public Date getAct_edate() {
		return act_edate;
	}

	public void setAct_edate(Date act_edate) {
		this.act_edate = act_edate;
	}

	public int getAct_awardcheck() {
		return act_awardcheck;
	}

	public void setAct_awardcheck(int act_awardcheck) {
		this.act_awardcheck = act_awardcheck;
	}

	public String getAct_actawardname() {
		return act_actawardname;
	}

	public void setAct_actawardname(String act_actawardname) {
		this.act_actawardname = act_actawardname;
	}

	public String getAct_description() {
		return act_description;
	}

	public void setAct_description(String act_description) {
		this.act_description = act_description;
	}

	@Override
	public String toString() {
		return "ActAwardVO [act_sdate=" + act_sdate + ", act_edate=" + act_edate + ", act_awardcheck=" + act_awardcheck
				+ ", act_actawardname=" + act_actawardname + ", act_description=" + act_description + "]";
	}
	
}
